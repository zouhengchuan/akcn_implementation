void basemul(int16_t c[2],
    const int16_t a[2],
    const int16_t b[2],
    int16_t zeta)
{
	int16_t temp[2];

	temp[0] = fqmul(a[0], b[0]);
	temp[1] = fqmul(a[1], b[1]);

	c[0] = fqmul(temp[1], zeta);
	c[0] += temp[0];

	c[1] = fqmul((a[0] + a[1]), (b[0] + b[1]));
	c[1] = c[1] - temp[0] - temp[1];
}

/********TFNTT_ntt********/
static void ntt_384(int16_t a[384]) {
	unsigned int start, j, k, len;
	int16_t t, s, r, zeta, zeta1, rho = zetas[0];

	for (j = 0; j < 192; ++j) {
		t = fqmul(zetas[1], a[j + 192]);
		a[j + 192] = a[j] + a[j + 192] - t;
		a[j] = a[j] + t;
	}

	k = 2;

	for (len = 96; len >= 3; len >>= 1)
	{
		for (start = 0; start < 384; start = j + len)
		{
			zeta = zetas[k++];
			for (j = start; j < start + len; ++j)
				{
					t = fqmul(zeta, a[j + len]);
					a[j + len] = (a[j] - t);
					a[j] = (a[j] + t);
				}
		}
	}

    for (start = 0; start < 384; start += 3) {
		zeta = zetas[k++];
		zeta1 = zetas[k++];
		for (j = start; j < start + 1; ++j) {
			t = fqmul(zeta, a[j + 1]);
			s = fqmul(zeta1, a[j + 2]);
			r = fqmul(rho, s - t);

			a[j + 1] = barrett_reduce(a[j] - s - r);
			a[j + 2] = barrett_reduce(a[j] - t + r);
			a[j] = barrett_reduce(a[j] + t + s);
		}
	}
}

void ntt(int16_t a[768])
{
	int i;
	int16_t a0[384], a1[384];

	for (i = 0; i < 384; i++)
	{
		a0[i] = a[2 * i];
		a1[i] = a[2 * i + 1];
	}
	ntt_384(a0);
	ntt_384(a1);

	for (i = 0; i < 384; i++)
	{
		a[2 * i] = a0[i];
		a[2 * i + 1] = a1[i];
	}

}

/********TFNTT_invntt********/
static void invntt_384(int16_t a[384]) {
	unsigned int start, j, k, len;
	int16_t t, s, r, zeta, zeta1, rho = zetas_inv[383];

	k = 0;

    for (start = 0; start < 384; start += 3) {
		zeta = zetas_inv[k++];
		zeta1 = zetas_inv[k++];
		for (j = start; j < start + 1; ++j) {
			r = fqmul(rho, a[j + 1] - a[j + 2]);
			t = a[j] - a[j + 2] + r;
			s = a[j] - a[j + 1] - r;

            a[j] = a[j] + a[j + 1] + a[j + 2];
			a[j + 1] = fqmul(zeta, t);
			a[j + 2] = fqmul(zeta1, s);
		}
	}

	for (len = 3; len <= 96; len <<= 1){
		for (start = 0; start < 384; start = j + len) {
			zeta = zetas_inv[k++];
			for (j = start; j < start + len; ++j) {
				t = a[j];
				a[j] = barrett_reduce(t + a[j + len]);
				a[j + len] = t - a[j + len];
				a[j + len] = fqmul(zeta, a[j + len]);
			}
		}
	}

	for (j = 0; j < 192; ++j) {
		t = a[j] - a[j + 192];
		t = fqmul(zetas_inv[382], t);
		a[j] = a[j] + a[j + 192];
		a[j] = a[j] - t;
		a[j] = fqmul(2568, a[j]); // 2^32 * 384^{-1} mod Q
		a[j + 192] = fqmul(1679, t); // 2^32 * 192^{-1} mod Q
	}
}

void invntt(int16_t a[768]) // 蒙哥马利域上的intt，同时退出蒙哥马利域
{
	int i;
	int16_t a0[384], a1[384];

	for (i = 0; i < 384; i++)
	{
		a0[i] = a[2 * i];
		a1[i] = a[2 * i + 1];
	}
	invntt_384(a0);
	invntt_384(a1);

	for (i = 0; i < 384; i++)
	{
		a[2 * i] = a0[i];
		a[2 * i + 1] = a1[i];
	}
}
